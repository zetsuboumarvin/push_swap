#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;

	if (c == '\0')
		return ((char *)s + ft_strlen((char *)s));
	str = (char *)s;
	while (*str != '\0')
	{
		if (*str == (char)c)
			return (str);
		str++;
	}
	return (NULL);
}
