void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	index;

	if (!s || !f)
		return ;
	index = 0;
	while (*s != '\0')
	{
		f(index, s);
		index++;
		s++;
	}
}
